import math
radius = eval(input("Enter a value for the radius: "))
length = eval(input("Enter a value for the length: "))
PI = 3.14159
area = PI * radius ** 2
volume = area * length
print("The area is", format(area, ".2f"))
print("The volume is", format(math.fabs(volume), ".2f"))

