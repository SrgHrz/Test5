# A variable that holds a boolean value(True/False) is
# known as a boolean variable
lightsOn = True

# True is represented by 1
# False is represented by 0
# You can use the int() function to convert the boolean values into
# an integer
print(int(True))
print(int(False))

# You can also use the bool() function to convert a number
# into a boolean value
print(bool(0))
print(bool(1))
print(bool(2))
# Any number other than 0 id true