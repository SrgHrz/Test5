# Q:Write a math statement that converts 37 degrees into radians
# and assign the answer to a variable (look at 2.1 computearea.py)
# Assign value to degrees
degrees = 37

import math  # to allow math.pi to work

radians = (math.pi / 180) * (degrees)

# Display result
print("The angle of", 37, "degrees is", radians, "radians")
