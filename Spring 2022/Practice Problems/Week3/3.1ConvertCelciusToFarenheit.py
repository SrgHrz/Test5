# Write a program that reads a temperature in Celsius,
# converts it to Fahrenheit, and displays the result,
# rounded to the nearest tenth of a degree.
# The formula for the conversion is as follows:
# fahrenheit = (9/5) * celsius + 32

celsius = eval(input("Enter a value for degrees centigrade: "))
fahrenheit = (9/5) * celsius + 32
print(format(fahrenheit, ".1f"))

