# Write a text-abbreviation translator for your old
# professor that translates each of these text-abbreviations
# to its full words. The text-abbreviations you should
# be able to translate are as follows: LOL - laugh out
# loud, BRB - be right back, NBD - no big deal, TBH - to be honest,
# IIRC - if I recall correctly, UT - University of Texas.

textAbbreviation = input("Enter text abbreviation: ")

if textAbbreviation == 'LOL':
    print("laugh out loud")
elif textAbbreviation == 'BRB':
    print("be right back")
elif textAbbreviation == 'NBD':
    print("no big deal")
elif textAbbreviation == 'TBH':
    print("to be honest")
elif textAbbreviation == 'IIRC':
    print("if I recall correctly")
elif textAbbreviation == 'UT':
    print("University of Texas")

