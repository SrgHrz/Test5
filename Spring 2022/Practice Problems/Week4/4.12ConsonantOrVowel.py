# Write a program that reads in a character of the alphabet
# and displays whether it is a vowel or a consonant.
# For our purposes, the letters a, e, i, o, and u are vowels,
# and all other letters are consonants. Your program
# should allow for letters to be inputted as uppercase or lowercase.

letter = input("Enter a letter: ")

if (letter == 'A') or (letter == 'a'):
    print("vowel")
elif (letter == 'E') or (letter == 'e'):
    print("vowel")
elif (letter == 'I') or (letter == 'i'):
    print("vowel")
elif (letter == 'O') or (letter == 'o'):
    print("vowel")
elif (letter == 'U') or (letter == 'u'):
    print("vowel")
else:
    print("consonant")

