# Calling a function executes the code in the function

# In a function's definition (the 1st def), you define what it
# is to do
# To use a function you have to use or invoke it (in the second def)
# The program that calls the function is called a "caller"
# There are two ways to call a function depending on whether or not
# the it returns a value
# If the function returns a value, a call to that function is
# usually treated as a value
# EX: larger = max(3,4) calls max(3,4) and assigns the result of
# the function to the variable larger
# If a function does not return a value, the call to the function must
# be a statement
# EX: the print() function