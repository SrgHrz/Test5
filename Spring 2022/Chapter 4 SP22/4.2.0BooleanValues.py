# A boolean value can either be "true" or "false"
# A boolean value is the result of a comparison operator
# Comparison operator: <, > , ==, <=, >=, !=,
# A value that holds a Boolean value is known as a boolean variable

# In this example, "radius" is our boolean variable
# We will use this along with a comparison operator to print a
# boolean value

radius = 1
print(radius > 0) # This will print True
print(radius < 0) # This will print False
print()

# Python uses:
# 1 to represent true and
# 0 to represent false
# Because of this we can use the int function to
# convert a boolean value into an integer

print(int(False)) # This will print 0
print(int(True)) # This will print 1