# Here is another example showing how a while loop works:

sum = 0
i = 1
while i < 10: # the loop is repeated a total of 9 times
    sum = sum + i # Adds the next value of i to the sum (now 1 b/c 0 + 1 = 1)
    i = i + 1 # Increments the value of i by 1 (now 2...)
    print("sum is", sum)  # sum is 1...