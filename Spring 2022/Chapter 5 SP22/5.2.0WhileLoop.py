# While loop executes statements repeatedly as long as the
# condition remains true

# Syntax for the while loop:

# while loop continuation condition:
    # loop body
    # Statement(s)

# Here is a simple while loop:
count = 0
while count < 100:
    print("Programming is fun!")
    count = count + 1

# The loop prints out the statement 100 times
# if you do <= 100 then the loop will be printed 101 times